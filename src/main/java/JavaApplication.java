import java.util.List;

public class JavaApplication {

    static User setupUser(UserService userService) {
        User user = userService.create("woody");
        return user;
    }

    static Channel setupChannel(ChannelService channelService) {
        Channel channel = channelService.create("공지");
        return channel;
    }

    static void messageCreateTest(MessageService messageService, Channel channel, User author) {
        Message message = messageService.create("안녕하세요.", channel.getId(), author.getId());
        System.out.println("메세지 생성: " + message.getId());
    }

    public static void main(String[] args) {
        // Repository 초기화
        UserRepository userRepository = new FileUserRepository("data/users");
        ChannelRepository channelRepository = new FileChannelRepository("data/channels");
        MessageRepository messageRepository = new FileMessageRepository("data/messages");

        // Service 초기화
        UserService userService = new BasicUserService(userRepository);
        ChannelService channelService = new BasicChannelService(channelRepository);
        MessageService messageService = new BasicMessageService(messageRepository, userRepository, channelRepository);

        User user = setupUser(userService);
        Channel channel = setupChannel(channelService);
        messageCreateTest(messageService, channel, user);
    }
}
